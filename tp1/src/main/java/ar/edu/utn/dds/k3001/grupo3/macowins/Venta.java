package ar.edu.utn.dds.k3001.grupo3.macowins;

public class Venta {
	private Prenda prendaVendida = null;
	private int cantidad = 0;
	private int fecha = 0;
	
	public double importeTotal(){
		return cantidad*(getPrendaVendida().precioFinal());
	}
	
	
	public Prenda getPrendaVendida() {
		return prendaVendida;
	}
	public void setPrendaVendida(Prenda prendaVendida) {
		this.prendaVendida = prendaVendida;
	}
	public int getCantidad() {
		return cantidad;
	}
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	public int getFecha() {
		return fecha;
	}
	public void setFecha(int fecha) {
		this.fecha = fecha;
	}

}
